public class T2VariableValues {
    public static void main(String[] args) {

        byte age=75;
        age=78;
        System.out.println(age);
        short price=1000;
        price=1100;
        System.out.println(price);
        int salary=120000;
        salary=1300000;
        System.out.println(salary);
        long distance=12545551112122L;
        distance=1251122;
        System.out.println(distance);
        float number1=-12521.1111F;
        number1=122.2f;
        System.out.println(number1);
        double num2=1325.3325;
        num2=12.2;
        System.out.println(num2);
        char symbol='&';
        symbol='4';
        System.out.println(symbol);
        boolean isRich=false;
        isRich=true;
        System.out.println(isRich);

        String city="London";
        city="new york";
        System.out.println(city);


    }
}
